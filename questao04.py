n1 = int(input('digite um numero: '))#pedindo o primeiro numero
n2 = int(input('digite outro numero: '))#pedindo o segundo numero

if n1 > n2:
    print("o numero 1 é maior que o 2")#verificando se o primeiro numero é maior que o segundo
elif n1 < n2: 
    print("o numero 1 é menor que o 2")#verificando se o primeiro numero é menor que o segundo
else: 
    print("os numeros sao iguais")#verificando se os numeros sao iguais